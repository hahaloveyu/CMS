package com.toptime.cmssync.entity;
/**
 * 元数据实体类
 * 
 */
public class MetadataInfo {
	private String domainMetadataCode; // 元数据组名称
	private String name; // 元数据名称
	private String key; // 元数据code
	private String value; // 元数据值


	public String getDomainMetadataCode() {
		return domainMetadataCode;
	}

	public void setDomainMetadataCode(String domainMetadataCode) {
		this.domainMetadataCode = domainMetadataCode;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
